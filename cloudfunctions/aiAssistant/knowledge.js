/**
 * 压力表检定专业知识库
 * 基于 JJG52-2013 弹性元件式一般压力表检定规程
 */

const KNOWLEDGE_BASE = `
## 压力表检定专业知识库

### 一、检定周期
1. 一般压力表的检定周期为6个月（半年）
2. 新购置的压力表首次使用前必须进行检定
3. 修理后的压力表必须重新检定
4. 长期停用后重新使用前需要检定

### 二、检定项目
1. 外观检查
   - 表盘清晰，无锈蚀、涂层脱落
   - 指针、表盘、表壳完好
   - 铭牌完整（型号、测量范围、准确度等级、出厂编号、制造厂名）
   
2. 示值误差检定
   - 检定点数：至少5个点（包含上限、下限）
   - 轻敲位移：轻敲表壳后示值变化不超过允许误差绝对值的1/2
   
3. 回程误差检定
   - 同一检定点正反行程的示值之差
   - 不应超过允许误差绝对值

### 三、准确度等级与允许误差
| 准确度等级 | 允许误差（%FS） |
|-----------|----------------|
| 1.0       | ±1.0           |
| 1.6       | ±1.6           |
| 2.5       | ±2.5           |
| 4.0       | ±4.0           |

### 四、检定不合格标准
以下情况判定为不合格：
1. 示值误差超过允许误差
2. 回程误差超过允许误差
3. 轻敲位移超过允许误差绝对值的1/2
4. 指针不能回到零点或零点误差超标
5. 外观有明显缺陷影响读数

### 五、压力表使用注意事项
1. 选型要求
   - 测量上限应为被测压力的1.5~3倍
   - 一般情况下选择测量上限为被测压力2倍的压力表
   
2. 安装要求
   - 垂直安装，倾斜角度不超过30°
   - 测量液体压力时，取压点应在管道下部
   - 测量气体压力时，取压点应在管道上部
   
3. 使用环境
   - 环境温度：-40℃~70℃（一般要求）
   - 避免振动、冲击
   - 远离热源

### 六、压力表更换标准
以下情况需要更换压力表：
1. 指针弯曲、折断或松动
2. 玻璃破碎或表盘模糊不清
3. 接头螺纹损坏
4. 连续两次检定不合格
5. 超过使用年限（一般为5-8年）

### 七、常见故障及处理
1. 指针不动：检查取压管是否堵塞
2. 指针跳动：系统有气泡或压力波动大
3. 示值偏高/偏低：需重新校准或更换
4. 压力表泄漏：检查密封垫片

### 八、相关法规
1. 《计量法》规定压力表属于强制检定的工作计量器具
2. 用于安全防护、贸易结算、医疗卫生、环境监测的压力表必须强制检定
3. 企业内部使用的一般压力表建议定期检定

### 九、压力容器压力表要求
1. 压力容器上的压力表量程应为最高工作压力的1.5~3倍
2. 表盘直径不小于100mm
3. 准确度等级不低于2.5级
4. 必须有检定合格证和铅封
`;

/**
 * 获取知识库内容
 */
function getKnowledgeBase() {
  return KNOWLEDGE_BASE;
}

/**
 * 根据问题类型获取相关知识片段
 */
function getRelevantKnowledge(question) {
  const lowerQ = question.toLowerCase();
  let relevantParts = [];

  // 检定周期相关
  if (lowerQ.includes('周期') || lowerQ.includes('多久') || lowerQ.includes('频率') || lowerQ.includes('几个月')) {
    relevantParts.push('检定周期：一般压力表检定周期为6个月（半年）。新购置、修理后、长期停用后重新使用的压力表都需要检定。');
  }

  // 不合格标准
  if (lowerQ.includes('不合格') || lowerQ.includes('合格') || lowerQ.includes('标准')) {
    relevantParts.push('检定不合格标准：示值误差超标、回程误差超标、轻敲位移超标、零点误差超标、外观缺陷影响读数。');
  }

  // 更换相关
  if (lowerQ.includes('更换') || lowerQ.includes('换') || lowerQ.includes('报废')) {
    relevantParts.push('更换标准：指针损坏、玻璃破碎、表盘模糊、接头损坏、连续两次检定不合格、超过使用年限（5-8年）。');
  }

  // 准确度等级
  if (lowerQ.includes('准确度') || lowerQ.includes('等级') || lowerQ.includes('误差')) {
    relevantParts.push('准确度等级与允许误差：1.0级(±1%)、1.6级(±1.6%)、2.5级(±2.5%)、4.0级(±4%)。');
  }

  // 选型
  if (lowerQ.includes('选型') || lowerQ.includes('量程') || lowerQ.includes('选择')) {
    relevantParts.push('选型要求：测量上限应为被测压力的1.5~3倍，一般选择2倍。');
  }

  // 安装
  if (lowerQ.includes('安装') || lowerQ.includes('位置')) {
    relevantParts.push('安装要求：垂直安装，倾斜不超30°。液体取压点在管道下部，气体取压点在管道上部。');
  }

  return relevantParts.length > 0 ? relevantParts.join('\n') : '';
}

module.exports = {
  getKnowledgeBase,
  getRelevantKnowledge
};
